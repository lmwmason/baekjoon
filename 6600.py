while True:
    try :
        x1, y1, x2, y2, x3, y3 = map(float, input().split())
        x1_2, x2_2, x3_2 = x1*x1, x2*x2, x3*x3
        y1_2, y2_2, y3_2 = y1*y1, y2*y2, y3*y3
        n = (x2_2 - x1_2 + y2_2 - y1_2) * (2*x1 - 2*x3) - (x3_2 - x1_2 + y3_2 - y1_2) * (2*x1 - 2*x2)
        d = (2*y1 - 2*y2) * (2*x1 - 2*x3) - (2*y1 - 2*y3) * (2*x1 - 2*x2)
        b = n / d
        if  (2*x1 - 2*x2)!=0:
            a = (x2_2 - x1_2 + y2_2 - y1_2 - (2*y1 - 2*y2)*b) / (2*x1 - 2*x2)
        else:
            a = (x3_2 - x1_2 + y3_2 - y1_2 - (2*y1 - 2*y3)*b) / (2*x1 - 2*x3)

        r = ((x1 + a)**2 + (y1 + b)**2)**0.5
        print(f'{2*3.141592653589793*r:.2f}')
    except :
        exit()